# atoms are bits of text that start with a
# colon, like :ok or :earth or :Today

# They can also contain underscores (_)
# and at symbols (@), like
# :this_is_a_short_sentence or :me@home

:'Today is a great day'

# Atoms have a value—it’s the same as their text:
#iex(1)> :test
#:test

# What makes atoms exciting is the way that they can
# combine with other types and Elixir’s pattern-matching
# techniques to build simple but powerful logical structures.